-- Batch 1: additive schema for configurable approval authority, closing text,
-- profile defaults, and profile-keyed signatures. Keep every legacy string
-- field/table path available during the migration period.

create table if not exists public.authority_titles (
  id bigint generated by default as identity primary key,
  title_th text not null,
  title_en text,
  sort_order integer not null default 100,
  is_active boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint authority_titles_title_th_not_blank_chk check (btrim(title_th) <> '')
);

alter table public.authority_titles
  add column if not exists title_th text,
  add column if not exists title_en text,
  add column if not exists sort_order integer not null default 100,
  add column if not exists is_active boolean not null default true,
  add column if not exists created_at timestamp with time zone not null default now(),
  add column if not exists updated_at timestamp with time zone not null default now();

create unique index if not exists authority_titles_title_th_uidx
  on public.authority_titles (lower(btrim(title_th)))
  where btrim(title_th) <> '';

create index if not exists authority_titles_active_sort_idx
  on public.authority_titles (is_active, sort_order, title_th);

alter table public.authority_limits
  add column if not exists authority_title_id bigint,
  add column if not exists is_unlimited boolean not null default false;

do $$
begin
  if to_regclass('public.authority_limits') is not null
     and not exists (
       select 1
       from pg_constraint
       where conname = 'authority_limits_authority_title_id_fkey'
         and conrelid = 'public.authority_limits'::regclass
     ) then
    alter table public.authority_limits
      add constraint authority_limits_authority_title_id_fkey
      foreign key (authority_title_id)
      references public.authority_titles(id)
      on delete restrict;
  end if;
end $$;

create unique index if not exists authority_limits_title_id_memo_type_uidx
  on public.authority_limits (authority_title_id, memo_type)
  where authority_title_id is not null;

create index if not exists authority_limits_title_id_idx
  on public.authority_limits (authority_title_id);

alter table public.user_profiles
  add column if not exists default_authority_title_id bigint,
  add column if not exists signature_data_url text;

do $$
begin
  if to_regclass('public.user_profiles') is not null
     and not exists (
       select 1
       from pg_constraint
       where conname = 'user_profiles_default_authority_title_id_fkey'
         and conrelid = 'public.user_profiles'::regclass
     ) then
    alter table public.user_profiles
      add constraint user_profiles_default_authority_title_id_fkey
      foreign key (default_authority_title_id)
      references public.authority_titles(id)
      on delete set null;
  end if;
end $$;

create index if not exists user_profiles_default_authority_title_id_idx
  on public.user_profiles (default_authority_title_id);

create table if not exists public.memo_closing_templates (
  memo_type text primary key,
  policy_ref text,
  has_authority_clause boolean not null default true,
  template_th text not null,
  supported_placeholders text[] not null default '{}'::text[],
  is_active boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint memo_closing_templates_memo_type_chk
    check (memo_type in ('sl', 'hw', 'int', 'ent', 'dep')),
  constraint memo_closing_templates_template_th_not_blank_chk
    check (btrim(template_th) <> '')
);

alter table public.memo_closing_templates
  add column if not exists policy_ref text,
  add column if not exists has_authority_clause boolean not null default true,
  add column if not exists template_th text,
  add column if not exists supported_placeholders text[] not null default '{}'::text[],
  add column if not exists is_active boolean not null default true,
  add column if not exists created_at timestamp with time zone not null default now(),
  add column if not exists updated_at timestamp with time zone not null default now();

create index if not exists memo_closing_templates_active_idx
  on public.memo_closing_templates (is_active, memo_type);

