select distinct btrim(al.title), null, 1000, true
from public.authority_limits al
where nullif(btrim(coalesce(al.title, '')), '') is not null
  and not exists (
    select 1
    from public.authority_titles at
    where lower(btrim(at.title_th)) = lower(btrim(al.title))
  );

update public.authority_limits al
set authority_title_id = at.id
from public.authority_titles at
where al.authority_title_id is null
  and lower(btrim(al.title)) = lower(btrim(at.title_th));

update public.user_profiles up
set default_authority_title_id = at.id
from public.authority_titles at
where up.default_authority_title_id is null
  and nullif(btrim(coalesce(up.title, '')), '') is not null
  and lower(btrim(up.title)) = lower(btrim(at.title_th));

